<?php
require_once('cls_conexion.model.php');
class Clase_Lavadoras
{
    public function todos()
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT * FROM `lavadoras`";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function uno($lavadoraId)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT * FROM `lavadoras` WHERE lavadoraId=$lavadoraId";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function insertar($modelo, $numserie, $tipolavadora, $precio, $fabrica)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "INSERT INTO `lavadoras`(`modelo`, `numserie`, `tipolavadora`, `precio`, `fabrica`)VALUES ( '$modelo', '$numserie', '$tipolavadora', $precio, '$fabrica')";
          
            $result = mysqli_query($con, $cadena);
            return 'ok';
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function actualizar($lavadoraId, $modelo, $numserie,$tipolavadora, $precio, $fabrica)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "UPDATE `lavadoras` SET `lavadoraId`='$lavadoraId',`modelo`='$modelo', `numserie`='$numserie',`tipolavadora`='$tipolavadora', `precio`=$precio, `fabrica`='$fabrica' WHERE `lavadoraId`='$lavadoraId'";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function eliminar($lavadoraId)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "delete from lavadoras where lavadoraId=$lavadoraId";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }



}
